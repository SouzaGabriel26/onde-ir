import type { Metadata } from 'next';
import { cookies } from 'next/headers';

import { ThemeProvider } from '@/components/ThemeProvider';
import { Toaster } from '@/components/ui/Toaster';

import { constants } from '../utils/constants';
import { feedbackMessage } from '../utils/feedbackMessage';
import './globals.css';

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

function getTheme() {
  const cookieStore = cookies();
  const themeFromCookie = cookieStore.get(constants.themeKey);
  return themeFromCookie?.value ?? 'dark';
}

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const theme = getTheme();

  const message = feedbackMessage.getFeedbackMessage();

  return (
    <html lang="en" className={theme} style={{ colorScheme: theme }}>
      <body className="h-screen text-slate-800 dark:text-white">
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          disableTransitionOnChange
          storageKey={constants.themeKey}
        >
          <Toaster message={message} />
          {children}
        </ThemeProvider>
      </body>
    </html>
  );
}
